# Defining the function bark
def bark(name, weight):
    if weight > 20:
        print(name, 'says WOOF WOOF!')
    else:
        print(name, 'says woof woof!')

# Invoking or calling the function multiple times with different parameters
bark('Codie', 40)
bark('Sparky', 9)
bark('Jackson', 12)
bark('Fido', 65)